                연간 결제 방식을 선택할 경우 사용료의 20% 할인 혜택이 적용됩니다.
            </li>
            <li>“고객”은 “서비스”에서 이용 중인 유료 서비스의 가격정책에 따라 선택한 정기 결제 주기(월간 또는 연간)에 맞춰 요금을 결제해야 합니다.</li>
            <li>정기 결제 시점에 결제가 실패되면 이후 2회까지 재결제를 시도하고 2회차 결제 시도까지 실패할 경우 “서비스” 이용이 중지됩니다.</li>
