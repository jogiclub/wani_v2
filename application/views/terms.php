            <li>“회사”는 새로운 “서비스”의 적용, 보안체계의 향상 및 유지, “회사”가 제공하는 새로운 “서비스”의 적용, 기타 “회사”의 업무상 중대한 사유에 의하여 약관을 변경하여야 할 필요가 있다고 판단될 경우 관계 법령을 위반하지 않는 범위 내에서 본 약관을 개정할 수 있습니다.</li>
